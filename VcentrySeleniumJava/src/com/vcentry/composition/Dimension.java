package com.vcentry.composition;

public class Dimension {
	int width;
	int height;
	int depth;
	
	public Dimension(int width, int height, int depth) {
		this.width = width;
		this.height = height;
		this.depth = depth;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getDepth() {
		return depth;
	}
	
	
	
}
